class BSStringUtils : NSObject {

    /**
     *  TODO: Consider deleting this method after app is ported.
     *  In Swift it is very simple and may not be worthwhile.
     */
    class func isStringEmpty(string: String) -> Bool {
        if (string == "") {
            return true
        } else {
            return false
        }
    }
    
